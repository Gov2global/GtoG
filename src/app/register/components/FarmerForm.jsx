function FarmerFormPage({ selectedType, selectedSubType, regLineID, regProfile }) {
  // [UNCHANGED] form state
  const [formData, setFormData] = useState({
    regName: "",
    regProfile: "",
    regSurname: "",
    regTel: "",
    regPlant: "",
    regPlantOther: "",
    regPlantSpecies: [],
    regPlantAmount: "",
    regPlantAge: "",
    areaRai: "",
    areaNgan: "",
    areaWa: "",
    province: "",
    district: "",
    sub_district: "",
    addressDetail: "",
    regType: "",
    regSubType: "",
  });
  const [plantOptions, setPlantOptions] = useState([]);
  const [provinces, setProvinces] = useState([]);
  const [districts, setDistricts] = useState([]);
  const [subDistricts, setSubDistricts] = useState([]);
  const [postcode, setPostcode] = useState("");
  const [showLoading, setShowLoading] = useState(false);
  const [errorMsg, setErrorMsg] = useState("");
  const [successMsg, setSuccessMsg] = useState("");
  const isSubmitting = useRef(false);

  // ‚úÖ handleChange (‡πÄ‡∏û‡∏¥‡πà‡∏°‡πÄ‡∏Ç‡πâ‡∏≤‡∏°‡∏≤)
  const handleChange = (field) => (valueOrEvent) => {
    const value = valueOrEvent?.target ? valueOrEvent.target.value : valueOrEvent;
    setFormData((prev) => ({ ...prev, [field]: value }));
  };

  // [UNCHANGED] autofill regProfile / regType / regSubType
  useEffect(() => {
    setFormData((prev) => ({ ...prev, regProfile: regProfile || prev.regProfile }));
  }, [regProfile]);
  useEffect(() => {
    setFormData((prev) => ({ ...prev, regType: selectedType || "", regSubType: selectedSubType || "" }));
  }, [selectedType, selectedSubType]);

  // [UNCHANGED] ‡πÇ‡∏´‡∏•‡∏î‡∏à‡∏±‡∏á‡∏´‡∏ß‡∏±‡∏î
  useEffect(() => {
    fetch("/api/farmer/get/province")
      .then((res) => res.json())
      .then((json) => json.success && setProvinces(json.data))
      .catch((err) => console.error("‚ùå ‡πÇ‡∏´‡∏•‡∏î‡∏à‡∏±‡∏á‡∏´‡∏ß‡∏±‡∏î‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß:", err));
  }, []);

  // [UNCHANGED] ‡πÇ‡∏´‡∏•‡∏î‡∏û‡∏∑‡∏ä
  useEffect(() => {
    fetch("/api/farmer/get/plant")
      .then((res) => res.json())
      .then((json) => {
        if (json.success) {
          const formatted = json.data.map((item) => ({ value: item.plantID, label: item.plantNameTH }));
          formatted.push({ value: "other", label: "‡∏≠‡∏∑‡πà‡∏ô‡πÜ (‡πÇ‡∏õ‡∏£‡∏î‡∏£‡∏∞‡∏ö‡∏∏)" });
          setPlantOptions(formatted);
        }
      })
      .catch((err) => console.error("‚ùå ‡πÇ‡∏´‡∏•‡∏î‡∏û‡∏∑‡∏ä‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß:", err));
  }, []);

  // ‚úÖ mapping plant ‚Üí variety
  const selectedPlantOption = plantOptions.find((opt) => opt.value === formData.regPlant);
  const selectedLabel = selectedPlantOption?.label || "";
  const mappedKey = plantLabelMap[selectedLabel] || "";
  const safeOptions = mappedKey ? plantVarieties[mappedKey].map((v) => ({ value: v, label: v })) : [];
  const safePlantSpecies = (formData.regPlantSpecies || []).map((sp) => ({ value: sp, label: sp }));
  const cleanLabel = selectedLabel || "‡∏û‡∏∑‡∏ä‡∏ó‡∏µ‡πà‡πÄ‡∏•‡∏∑‡∏≠‡∏Å";

  // [UNCHANGED] calculateTotalAreaSqm
  const calculateTotalAreaSqm = () => {
    const rai = parseFloat(formData.areaRai || 0) * 1600;
    const ngan = parseFloat(formData.areaNgan || 0) * 400;
    const wa = parseFloat(formData.areaWa || 0) * 4;
    return rai + ngan + wa;
  };

  // [UNCHANGED] validate
  const validate = () => {
    if (!formData.regName) return "‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡∏ä‡∏∑‡πà‡∏≠";
    if (!formData.regTel) return "‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡πÄ‡∏ö‡∏≠‡∏£‡πå‡πÇ‡∏ó‡∏£";
    if (!formData.regPlant) return "‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏û‡∏∑‡∏ä‡∏ó‡∏µ‡πà‡∏õ‡∏•‡∏π‡∏Å";
    return null;
  };

  // [CHANGED] handleSubmit ‡πÄ‡∏û‡∏¥‡πà‡∏° reset form
  const handleSubmit = async (e) => {
    e.preventDefault();
    if (isSubmitting.current) return;
    setErrorMsg(""); setSuccessMsg("");
    const validateMsg = validate();
    if (validateMsg) { setErrorMsg(validateMsg); return; }

    setShowLoading(true); isSubmitting.current = true;
    try {
      const idRes = await fetch(`/api/farmer/gen-id?regType=${formData.regType}`);
      const idJson = await idRes.json();
      if (!idJson.success) throw new Error("‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏£‡∏´‡∏±‡∏™‡πÄ‡∏Å‡∏©‡∏ï‡∏£‡∏Å‡∏£‡πÑ‡∏î‡πâ");

      const payload = {
        ...formData,
        regID: idJson.regID,
        regPlantSpecies: formData.regPlantSpecies.filter(Boolean),
        postcode,
        totalAreaSqm: calculateTotalAreaSqm(),
        regLineID,
      };

      const submitRes = await fetch("/api/farmer/submit/farmer", {
        method: "POST", headers: { "Content-Type": "application/json" }, body: JSON.stringify(payload),
      });
      const submitJson = await submitRes.json();
      if (!submitJson.success) throw new Error(submitJson.message || "‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß");

      await fetch("/api/farmer/line/set-richmenu", {
        method: "POST", headers: { "Content-Type": "application/json" }, body: JSON.stringify({ regLineID }),
      });

      setSuccessMsg("‚úÖ ‡∏•‡∏á‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à!");
      setFormData({
        regName: "", regProfile: "", regSurname: "", regTel: "", regPlant: "", regPlantOther: "", regPlantSpecies: [],
        regPlantAmount: "", regPlantAge: "", areaRai: "", areaNgan: "", areaWa: "", province: "", district: "", sub_district: "",
        addressDetail: "", regType: "", regSubType: "",
      });

      setTimeout(() => {
        setShowLoading(false);
        if (window?.liff) window.liff.closeWindow();
        else if (liff?.closeWindow) liff.closeWindow();
      }, 800);
    } catch (err) {
      setErrorMsg("‚ùå " + (err.message || "‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î"));
      setShowLoading(false);
    } finally { isSubmitting.current = false; }
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-gradient-to-tr from-yellow-100 via-white to-yellow-200 p-4">
      <div className="w-full max-w-lg bg-white shadow-2xl rounded-3xl px-8 py-10">
        <h2 className="text-3xl font-extrabold text-center text-amber-800 mb-8 flex items-center justify-center gap-3">
          <GiFarmTractor size={45} className="animate-bounce-slow" /> ‡∏•‡∏á‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô‡πÄ‡∏Å‡∏©‡∏ï‡∏£‡∏Å‡∏£
        </h2>

        {errorMsg && <div className="mb-4 text-red-700 bg-red-100 px-4 py-2 rounded-lg">{errorMsg}</div>}
        {successMsg && <div className="mb-4 text-green-700 bg-green-100 px-4 py-2 rounded-lg">{successMsg}</div>}

        <form onSubmit={handleSubmit} className="space-y-6">
          <ModernInput label="‡∏ä‡∏∑‡πà‡∏≠" value={formData.regName} onChange={handleChange("regName")} />
          <ModernInput label="‡∏ô‡∏≤‡∏°‡∏™‡∏Å‡∏∏‡∏•" value={formData.regSurname} onChange={handleChange("regSurname")} />
          <ModernInput label="‡πÄ‡∏ö‡∏≠‡∏£‡πå‡πÇ‡∏ó‡∏£" value={formData.regTel} onChange={handleChange("regTel")} />
          <ModernInput label="ID LINE" value={formData.regProfile} onChange={handleChange("regProfile")} />

          <ModernSelect label="‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏û‡∏∑‡∏ä‡∏ó‡∏µ‡πà‡∏õ‡∏•‡∏π‡∏Å" value={formData.regPlant} onChange={handleChange("regPlant")} options={plantOptions} />

          {formData.regPlant === "other" && (
            <ModernInput label="‡∏£‡∏∞‡∏ö‡∏∏‡∏û‡∏∑‡∏ä‡∏≠‡∏∑‡πà‡∏ô‡πÜ" value={formData.regPlantOther} onChange={handleChange("regPlantOther")} />
          )}

          {formData.regPlant && (
            <>
              {mappedKey && plantVarieties[mappedKey] ? (
                <ModernCreatableSelect
                  label={`‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏´‡∏£‡∏∑‡∏≠‡∏û‡∏¥‡∏°‡∏û‡πå‡∏û‡∏±‡∏ô‡∏ò‡∏∏‡πå‡∏Ç‡∏≠‡∏á "${selectedLabel}"`}
                  value={safePlantSpecies}
                  onChange={(val) => setFormData((prev) => ({ ...prev, regPlantSpecies: val.map((v) => v.value) }))}
                  options={safeOptions}
                  isMulti
                />
              ) : (
                <ModernInput label={`‡πÇ‡∏õ‡∏£‡∏î‡∏£‡∏∞‡∏ö‡∏∏‡∏û‡∏±‡∏ô‡∏ò‡∏∏‡πå‡∏Ç‡∏≠‡∏á "${cleanLabel}"`} value={formData.regPlantSpecies?.[0] || ""} onChange={(v) => setFormData((prev) => ({ ...prev, regPlantSpecies: [v] }))} />
              )}

              <ModernInput label="‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏ó‡∏µ‡πà‡∏õ‡∏•‡∏π‡∏Å (‡∏ï‡πâ‡∏ô)" value={formData.regPlantAmount} onChange={handleChange("regPlantAmount")} />
              <ModernInput label="‡∏≠‡∏≤‡∏¢‡∏∏‡∏Ç‡∏≠‡∏á‡∏û‡∏∑‡∏ä" value={formData.regPlantAge} onChange={handleChange("regPlantAge")} />

              <h3 className="text-xl font-semibold text-amber-700 mb-2 mt-4">‡∏û‡∏∑‡πâ‡∏ô‡∏ó‡∏µ‡πà‡∏ó‡∏µ‡πà‡∏õ‡∏•‡∏π‡∏Å</h3>
              <div className="grid grid-cols-3 gap-4">
                <ModernInput label="‡πÑ‡∏£‡πà" value={formData.areaRai} onChange={handleChange("areaRai")} type="number" />
                <ModernInput label="‡∏á‡∏≤‡∏ô" value={formData.areaNgan} onChange={handleChange("areaNgan")} type="number" />
                <ModernInput label="‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏ß‡∏≤" value={formData.areaWa} onChange={handleChange("areaWa")} type="number" />
              </div>
              <p className="text-sm">üßÆ ‡∏£‡∏ß‡∏°‡∏û‡∏∑‡πâ‡∏ô‡∏ó‡∏µ‡πà‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î: <strong>{calculateTotalAreaSqm()}</strong> ‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡πÄ‡∏°‡∏ï‡∏£</p>

              {/* ‚úÖ mock options ‡∏à‡∏±‡∏á‡∏´‡∏ß‡∏±‡∏î-‡∏≠‡∏≥‡πÄ‡∏†‡∏≠-‡∏ï‡∏≥‡∏ö‡∏• */}
              <ModernSelect label="‡∏à‡∏±‡∏á‡∏´‡∏ß‡∏±‡∏î" value={formData.province} onChange={handleChange("province")} options={provinces.map((p) => ({ value: p.province, label: p.province }))} />
              {formData.province && <ModernSelect label="‡∏≠‡∏≥‡πÄ‡∏†‡∏≠" value={formData.district} onChange={handleChange("district")} options={districts.map((d) => ({ value: d, label: d }))} />}
              {formData.district && <ModernSelect label="‡∏ï‡∏≥‡∏ö‡∏•" value={formData.sub_district} onChange={handleChange("sub_district")} options={subDistricts.map((s) => ({ value: s, label: s }))} />}
              {formData.sub_district && (
                <>
                  <ModernInput label="‡∏£‡∏´‡∏±‡∏™‡πÑ‡∏õ‡∏£‡∏©‡∏ì‡∏µ‡∏¢‡πå" value={postcode} onChange={(val) => setPostcode(val)} />
                  <ModernInput label="‡∏ó‡∏µ‡πà‡∏≠‡∏¢‡∏π‡πà‡πÄ‡∏û‡∏¥‡πà‡∏°‡πÄ‡∏ï‡∏¥‡∏°" value={formData.addressDetail} onChange={handleChange("addressDetail")} />
                </>
              )}
            </>
          )}

          <button type="submit" disabled={showLoading} className="w-full flex items-center justify-center gap-2 bg-gradient-to-r from-amber-700 to-yellow-600 text-white py-3 rounded-full">
            <DiCoda size={22} /> ‡∏•‡∏á‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô
          </button>
        </form>
      </div>
      {showLoading && <LoadingOverlay />}
    </div>
  );
}

export default FarmerFormPage;
